#!/usr/bin/env python3
"""
Environment Setup Helper for Recipe Automation System
Helps users set up their .env file with API keys
"""

import os
import sys
from pathlib import Path

def create_env_file():
    """Create .env file with user input"""
    
    print("ğŸ”§ Recipe Automation System - Environment Setup")
    print("=" * 50)
    
    # Check if .env already exists
    if os.path.exists('.env'):
        print("âš ï¸  .env file already exists!")
        response = input("Do you want to overwrite it? (y/N): ")
        if response.lower() != 'y':
            print("Setup cancelled.")
            return False
    
    print("\nğŸ“ Setting up environment variables...")
    print("You'll need your OpenAI API key for this system to work.")
    print("Get it from: https://platform.openai.com/api-keys")
    print()
    
    # Get OpenAI API key
    openai_key = input("Enter your OpenAI API key: ").strip()
    if not openai_key:
        print("âŒ OpenAI API key is required!")
        return False
    
    # Get USDA API key (required)
    print("\nUSDA API key is required for nutrition analysis")
    print("Get it from: https://fdc.nal.usda.gov/api-key-signup.html")
    usda_key = input("Enter your USDA API key: ").strip()
    if not usda_key:
        print("âŒ USDA API key is required!")
        return False
    
    # Create .env file
    env_content = f"""# Recipe Automation System - Environment Variables
# Generated by setup_env.py

# OpenAI API Key (Required for Vision API OCR and content generation)
OPENAI_API_KEY={openai_key}

# USDA API Key (Required for nutrition analysis)
USDA_API_KEY={usda_key}

# Optional: Override default settings
# OPENAI_MODEL=gpt-4
# OPENAI_TEMPERATURE=0.4
# BATCH_SIZE=5
"""
    
    try:
        with open('.env', 'w') as f:
            f.write(env_content)
        
        print("\nâœ… .env file created successfully!")
        print("ğŸ“ File location: .env")
        print("\nğŸ”’ Security note: .env is already in .gitignore and won't be committed to git")
        
        return True
        
    except Exception as e:
        print(f"âŒ Failed to create .env file: {e}")
        return False

def test_environment():
    """Test if environment variables are loaded correctly"""
    print("\nğŸ§ª Testing environment setup...")
    
    try:
        from dotenv import load_dotenv
        load_dotenv()
        
        openai_key = os.getenv('OPENAI_API_KEY')
        usda_key = os.getenv('USDA_API_KEY')
        
        if openai_key:
            print("âœ… OPENAI_API_KEY found")
        else:
            print("âŒ OPENAI_API_KEY not found")
            return False
        
        if usda_key:
            print("âœ… USDA_API_KEY found")
        else:
            print("âŒ USDA_API_KEY not found")
            return False
        
        return True
        
    except Exception as e:
        print(f"âŒ Environment test failed: {e}")
        return False

def main():
    """Main setup function"""
    
    # Create .env file
    if not create_env_file():
        sys.exit(1)
    
    # Test environment
    if not test_environment():
        print("\nâŒ Environment setup failed!")
        sys.exit(1)
    
    print("\nğŸ‰ Environment setup complete!")
